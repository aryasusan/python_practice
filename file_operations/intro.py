#     -to read data from a file
#     -represented using the symbol r
#     - file will be read line by line
#
# Write operation:
#     -to write data into a file
#     -represented using w
#
# Append Operation:
#     -to update a file
#     -represented using a
#
# Steps:
#
# 1.Create a file reference:
#     - variable_name = open('arg1','arg2')
#     - arg1- location of the file to read/write/append
#     - arg2- mode of operation r/w/a