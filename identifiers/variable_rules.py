##set of rules for creating a variable

# variable name should not start with a space
# space should not be given in between ==> full name ==> not supported
# underscore can be given beginning, in between ==> _name, full_name
# should not start with a number, number can be given in-between or end
# reserved keywords should not be given as name ==> input, print, if, while, break, for, else
# case sensitive
# give meaningful names

# multiple line commenting ===> select all and ctrl+/
# num=10
# name='arya'
# age=30

